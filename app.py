def ensure_action_sheet_schema(sheet) -> None:
    existing_header = [header.strip() for header in sheet.row_values(1)]

    if not existing_header:
        sheet.append_row(ACTION_SHEET_COLUMNS)
        return

    for column_index, header in enumerate(ACTION_SHEET_COLUMNS, start=1):
        if len(existing_header) < column_index or existing_header[column_index - 1] != header:
            sheet.update_cell(1, column_index, header)


def normalize_action_dataframe(action_df: pd.DataFrame) -> pd.DataFrame:
    normalized_df = action_df.copy()

    if normalized_df.empty:
        return pd.DataFrame(columns=ACTION_SHEET_COLUMNS)

    if "Action" not in normalized_df.columns:
        if "Action Item" in normalized_df.columns:
            normalized_df["Action"] = normalized_df["Action Item"]
        elif "Task" in normalized_df.columns:
            normalized_df["Action"] = normalized_df["Task"]
        else:
            normalized_df["Action"] = ""

    if "Priority" not in normalized_df.columns:
        if "Status" in normalized_df.columns:
            status_series = normalized_df["Status"].astype(str).str.strip().str.lower()
            normalized_df["Priority"] = status_series.map(
                {
                    "open": "Medium",
                    "in progress": "High",
                    "done": "Low",
                    "high": "High",
                    "medium": "Medium",
                    "low": "Low",
                }
            ).fillna("Medium")
        else:
            normalized_df["Priority"] = "Medium"

    if "Source" not in normalized_df.columns:
        normalized_df["Source"] = TEAM_ACTION_SOURCE

    normalized_df["Action"] = normalized_df["Action"].astype(str).str.strip()
    normalized_df["Priority"] = normalized_df["Priority"].astype(str).str.strip().str.title()
    normalized_df["Source"] = normalized_df["Source"].astype(str).str.strip()
    normalized_df.loc[normalized_df["Source"] == "", "Source"] = TEAM_ACTION_SOURCE
    normalized_df.loc[
        ~normalized_df["Source"].isin([TEAM_ACTION_SOURCE, AI_ACTION_SOURCE]),
        "Source",
    ] = TEAM_ACTION_SOURCE
    normalized_df = normalized_df[normalized_df["Action"] != ""].copy()
    normalized_df.loc[~normalized_df["Priority"].isin(["High", "Medium", "Low"]), "Priority"] = "Medium"
    return normalized_df[ACTION_SHEET_COLUMNS]


def render_question_timer(question: str, duration_seconds: int, timer_reset_key: str) -> None:
    if duration_seconds <= 0:
        return

    beep_base64 = base64.b64encode(generate_timer_end_sound()).decode()
    storage_seed = f"{question}|{duration_seconds}|{timer_reset_key}"

    components.html(
        f"""
        <div style=\"padding: 0.75rem 1rem; border-radius: 0.75rem; background: #fff7ed; border: 1px solid #fdba74; color: #9a3412; font-family: sans-serif;\">
            <div style=\"font-size: 0.85rem; margin-bottom: 0.2rem;\">Question timer</div>
            <div id=\"retro-timer-display\" style=\"font-size: 1.8rem; font-weight: 700; letter-spacing: 0.04em;\">--:--</div>
            <div id=\"retro-timer-status\" style=\"font-size: 0.85rem; margin-top: 0.2rem;\">Beep will play when time finishes.</div>
        </div>
        <script>
        const durationSeconds = {duration_seconds};
        const seed = {json.dumps(storage_seed)};
        const timerKey = `retro-question-timer-${{seed}}`;
        const timerDoneKey = `retro-question-timer-done-${{seed}}`;
        const display = document.getElementById('retro-timer-display');
        const status = document.getElementById('retro-timer-status');
        const endSound = new Audio('data:audio/wav;base64,{beep_base64}');
        let endTime = Number(window.localStorage.getItem(timerKey) || 0);

        if (!endTime || Number.isNaN(endTime) || endTime <= Date.now()) {{
            endTime = Date.now() + durationSeconds * 1000;
            window.localStorage.setItem(timerKey, String(endTime));
            window.localStorage.removeItem(timerDoneKey);
        }}

        function formatRemaining(totalSeconds) {{
            const safeSeconds = Math.max(0, totalSeconds);
            const minutes = String(Math.floor(safeSeconds / 60)).padStart(2, '0');
            const seconds = String(safeSeconds % 60).padStart(2, '0');
            return `${{minutes}}:${{seconds}}`;
        }}

        function tick() {{
            const remainingMs = endTime - Date.now();
            const remainingSeconds = Math.ceil(remainingMs / 1000);

            if (remainingMs <= 0) {{
                display.textContent = '00:00';
                status.textContent = 'Time is up.';
                if (!window.localStorage.getItem(timerDoneKey)) {{
                    window.localStorage.setItem(timerDoneKey, '1');
                    endSound.play().catch(() => {{}});
                }}
                return;
            }}

            display.textContent = formatRemaining(remainingSeconds);
            status.textContent = remainingSeconds <= 10 ? 'Finishing soon...' : 'Beep will play when time finishes.';
        }}

        tick();
        window.setInterval(tick, 250);
        </script>
        """,
        height=110,
    )


def ensure_spill_over_column(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    if all(col in df.columns for col in ["Committed", "Scope Added", "Completed"]):
        df["Spill Over"] = (
            pd.to_numeric(df["Committed"], errors="coerce").fillna(0)
            + pd.to_numeric(df["Scope Added"], errors="coerce").fillna(0)
            - pd.to_numeric(df["Completed"], errors="coerce").fillna(0)
        )
    return df


