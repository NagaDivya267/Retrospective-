def get_openai_api_key() -> tuple[str | None, str, list[str]]:
    """Resolve OpenAI API key across local env and multiple Streamlit secrets layouts."""
    secrets = get_streamlit_secrets()
    candidates: list[tuple[str | None, str]] = [
        (os.getenv("OPENAI_API_KEY"), "env:OPENAI_API_KEY"),
    ]

    secret_keys: list[str] = []
    try:
        secret_keys = list(secrets.keys())
    except Exception:
        secret_keys = []

    if "OPENAI_API_KEY" in secrets:
        candidates.append((secrets.get("OPENAI_API_KEY"), "secrets:OPENAI_API_KEY"))
    if "openai_api_key" in secrets:
        candidates.append((secrets.get("openai_api_key"), "secrets:openai_api_key"))
    if "api_key" in secrets:
        candidates.append((secrets.get("api_key"), "secrets:api_key"))

    if "openai" in secrets:
        openai_section = secrets["openai"]
        if hasattr(openai_section, "get"):
            candidates.extend(
                [
                    (openai_section.get("api_key"), "secrets:openai.api_key"),
                    (openai_section.get("OPENAI_API_KEY"), "secrets:openai.OPENAI_API_KEY"),
                ]
            )

    # Last resort: scan nested secrets recursively for key-like fields.
    def scan_mapping(mapping, prefix: str = "secrets"):
        discovered: list[tuple[str | None, str]] = []
        if not hasattr(mapping, "items"):
            return discovered
        for key, value in mapping.items():
            key_str = str(key)
            lower_key = key_str.lower()
            path = f"{prefix}.{key_str}"
            if lower_key in {"openai_api_key", "api_key"}:
                discovered.append((value, path))
            if hasattr(value, "items"):
                discovered.extend(scan_mapping(value, path))
        return discovered

    candidates.extend(scan_mapping(secrets))

    for value, source in candidates:
        if value and str(value).strip():
            return str(value).strip(), source, secret_keys

    return None, "not-found", secret_keys


@st.cache_data(show_spinner=False)
def generate_spin_sound() -> bytes:
    """Synthesise a short spinning-wheel whoosh as WAV bytes (stdlib only)."""
    sample_rate = 44100
    duration = 1.5
    num_samples = int(sample_rate * duration)
    buf = io.BytesIO()
    with wave.open(buf, "w") as wav:
        wav.setnchannels(1)
        wav.setsampwidth(2)
        wav.setframerate(sample_rate)
        frames = []
        for i in range(num_samples):
            t = i / sample_rate
            progress = t / duration
            # frequency sweeps 800 Hz → 200 Hz (wheel slowing down)
            freq = 800 * (1 - progress) + 200 * progress
            # amplitude envelope peaks in the middle
            amplitude = 32767 * math.sin(math.pi * progress) * 0.7
            sample = int(amplitude * math.sin(2 * math.pi * freq * t))
            frames.append(struct.pack("<h", max(-32768, min(32767, sample))))
        wav.writeframes(b"".join(frames))
    return buf.getvalue()


@st.cache_data(show_spinner=False)
def generate_timer_end_sound() -> bytes:
    """Create a short beep used when a question timer completes."""
    sample_rate = 44100
    duration = 0.8
    frequency = 880
    num_samples = int(sample_rate * duration)
    buf = io.BytesIO()
    with wave.open(buf, "w") as wav:
        wav.setnchannels(1)
        wav.setsampwidth(2)
        wav.setframerate(sample_rate)
        frames = []
        for i in range(num_samples):
            t = i / sample_rate
            fade_multiplier = min(1.0, t * 10) * min(1.0, (duration - t) * 10)
            amplitude = 32767 * 0.35 * fade_multiplier
            sample = int(amplitude * math.sin(2 * math.pi * frequency * t))
            frames.append(struct.pack("<h", max(-32768, min(32767, sample))))
        wav.writeframes(b"".join(frames))
    return buf.getvalue()


def get_credentials_file_path() -> str:
    base_dir = os.path.dirname(os.path.abspath(__file__))
    candidate_paths = [
        os.path.join(base_dir, "credentials.json"),
        os.path.join(base_dir, "..", "credentials.json"),
        os.path.join(base_dir, "..", "..", "credentials.json"),
        os.path.join(base_dir, "..", "..", "..", "credentials.json"),
    ]

    for candidate_path in candidate_paths:
        resolved_path = os.path.abspath(candidate_path)
        if os.path.exists(resolved_path):
            return resolved_path

    raise FileNotFoundError(
        "credentials.json was not found. Add it near the app or configure st.secrets['gcp_service_account']."
    )


@st.cache_resource(show_spinner=False)
def get_google_workbook():
    secrets = get_streamlit_secrets()

    if "gcp_service_account" in secrets:
        credentials = Credentials.from_service_account_info(
            dict(secrets["gcp_service_account"]),
            scopes=GOOGLE_SHEETS_SCOPE,
        )
    else:
        credentials = Credentials.from_service_account_file(
            get_credentials_file_path(),
            scopes=GOOGLE_SHEETS_SCOPE,
        )

    client = gspread.authorize(credentials)
    return client.open(GOOGLE_SHEET_NAME)


@st.cache_resource(show_spinner=False)
def get_or_create_worksheet(title: str, rows: int = 100, cols: int = 20):
    workbook = get_google_workbook()
    try:
        return workbook.worksheet(title)
    except gspread.WorksheetNotFound:
        return workbook.add_worksheet(title=title, rows=rows, cols=cols)


@st.cache_data(show_spinner=False, ttl=30)
def get_sheet_records(title: str, rows: int = 100, cols: int = 20) -> list[dict]:
    sheet = get_or_create_worksheet(title, rows=rows, cols=cols)
    return sheet.get_all_records()


@st.cache_data(show_spinner=False, ttl=30)
def get_sheet_row_values(title: str, row_number: int, rows: int = 100, cols: int = 20) -> list[str]:
    sheet = get_or_create_worksheet(title, rows=rows, cols=cols)
    return sheet.row_values(row_number)


@st.cache_data(show_spinner=False, ttl=30)
def get_sheet_cell_value(title: str, cell_ref: str, rows: int = 100, cols: int = 20) -> str:
    sheet = get_or_create_worksheet(title, rows=rows, cols=cols)
    return sheet.acell(cell_ref).value or ""


def clear_google_sheet_read_cache() -> None:
    for cached_reader in [get_sheet_records, get_sheet_row_values, get_sheet_cell_value]:
        clear_method = getattr(cached_reader, "clear", None)
        if callable(clear_method):
            clear_method()


def set_flash_message(scope: str, level: str, message: str) -> None:
    st.session_state[f"flash_{scope}"] = {"level": level, "message": message}


def show_flash_message(scope: str) -> None:
    flash_message = st.session_state.pop(f"flash_{scope}", None)
    if not flash_message:
        return

    level = str(flash_message.get("level", "info"))
    message = str(flash_message.get("message", ""))
    if level == "success":
        st.success(message)
    elif level == "warning":
        st.warning(message)
    elif level == "error":
        st.error(message)
    else:
        st.info(message)


def apply_pending_session_resets() -> None:
    pending_keys = st.session_state.pop("_pending_session_resets", [])
    for key in pending_keys:
        if key in st.session_state:
            value = st.session_state[key]
            if isinstance(value, str):
                st.session_state[key] = ""
            elif isinstance(value, int):
                st.session_state[key] = 0
            elif isinstance(value, float):
                st.session_state[key] = 0.0
            elif isinstance(value, list):
                st.session_state[key] = []


def clear_session_keys(*keys: str) -> None:
    pending_keys = st.session_state.setdefault("_pending_session_resets", [])
    for key in keys:
        if key not in pending_keys:
            pending_keys.append(key)


