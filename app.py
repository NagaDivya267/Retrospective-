def get_mood_score_by_sprint_name() -> dict[str, float]:
    mood_records = get_sheet_records(MOOD_WORKSHEET_NAME, rows=500, cols=10)
    if not mood_records:
        return {}

    mood_df = pd.DataFrame(mood_records)
    required_cols = {"PI or Sprint Name", "Average Mood Score"}
    if not required_cols.issubset(set(mood_df.columns)):
        return {}

    mood_df = mood_df.copy()
    mood_df["PI or Sprint Name"] = mood_df["PI or Sprint Name"].astype(str).str.strip()
    mood_df["Average Mood Score"] = pd.to_numeric(mood_df["Average Mood Score"], errors="coerce")
    mood_df = mood_df.dropna(subset=["Average Mood Score"])
    mood_df = mood_df[mood_df["PI or Sprint Name"] != ""]

    if mood_df.empty:
        return {}

    grouped = mood_df.groupby("PI or Sprint Name", as_index=False)["Average Mood Score"].mean()
    return {
        str(row["PI or Sprint Name"]): float(row["Average Mood Score"])
        for _, row in grouped.iterrows()
    }


def prepare_retro_health_dataframe(source_df: pd.DataFrame) -> tuple[pd.DataFrame | None, list[str]]:
    sprint_col = find_matching_column(source_df, ["Sprint Name", "Sprint"])
    committed_col = find_matching_column(source_df, ["Committed SP", "Committed"])
    completed_col = find_matching_column(source_df, ["Completed SP", "Completed"])
    defects_col = find_matching_column(source_df, ["Defects #", "Defects", "Defect", "Defect Count"])

    missing_fields: list[str] = []
    if not sprint_col:
        missing_fields.append("Sprint Name")
    if not committed_col:
        missing_fields.append("Committed SP")
    if not completed_col:
        missing_fields.append("Completed SP")
    if not defects_col:
        missing_fields.append("Defects #")

    if missing_fields:
        return None, missing_fields

    retro_df = pd.DataFrame(
        {
            "Sprint Name": source_df[sprint_col].astype(str).str.strip(),
            "Committed SP": pd.to_numeric(source_df[committed_col], errors="coerce").fillna(0),
            "Completed SP": pd.to_numeric(source_df[completed_col], errors="coerce").fillna(0),
            "Defects #": pd.to_numeric(source_df[defects_col], errors="coerce").fillna(0),
        }
    )

    retro_df = retro_df[retro_df["Sprint Name"] != ""].copy()
    retro_df = retro_df.reset_index(drop=True)

    reliability_pct = []
    spillover_pct = []
    defect_increase_pct = []
    velocity_change_pct = []

    previous_defects = None
    previous_velocity = None

    for _, row in retro_df.iterrows():
        committed = float(row["Committed SP"])
        completed = float(row["Completed SP"])
        defects = float(row["Defects #"])

        reliability = (completed / committed) * 100 if committed > 0 else 0.0
        spillover = ((committed - completed) / committed) * 100 if committed > 0 else 0.0

        if previous_defects is None:
            defect_increase = 0.0
        elif previous_defects == 0:
            defect_increase = 0.0
        else:
            defect_increase = ((defects - previous_defects) / previous_defects) * 100

        if previous_velocity is None:
            velocity_change = 0.0
        elif previous_velocity == 0:
            velocity_change = 0.0
        else:
            velocity_change = ((completed - previous_velocity) / previous_velocity) * 100

        reliability_pct.append(reliability)
        spillover_pct.append(spillover)
        defect_increase_pct.append(defect_increase)
        velocity_change_pct.append(velocity_change)

        previous_defects = defects
        previous_velocity = completed

    retro_df["Reliability %"] = reliability_pct
    retro_df["Spillover %"] = spillover_pct
    retro_df["Defect Increase %"] = defect_increase_pct
    retro_df["Velocity Change %"] = velocity_change_pct

    retro_df["Delivery Score"] = retro_df["Reliability %"].apply(score_delivery)
    retro_df["Quality Score"] = retro_df["Defect Increase %"].apply(score_quality)
    retro_df["Inventory Score"] = retro_df["Spillover %"].apply(score_inventory)
    retro_df["Productivity Score"] = retro_df["Velocity Change %"].apply(score_productivity)

    return retro_df, []


def pick_weakest_category(category_scores: dict[str, float]) -> str:
    min_score = min(category_scores.values())
    lowest_categories = [name for name, score in category_scores.items() if score == min_score]

    for category in RETRO_PRIORITY_ORDER:
        if category in lowest_categories:
            return category

    return lowest_categories[0]


def get_ai_retro_recommendation(category_scores: dict[str, float]) -> dict[str, str]:
    selected_category = pick_weakest_category(category_scores)
    recommended_retro = RETRO_CATEGORY_MAP[selected_category]
    reason = RETRO_REASON_MAP[selected_category]

    sorted_scores = sorted(category_scores.values())
    if len(sorted_scores) < 2:
        confidence = "High"
    else:
        gap = sorted_scores[1] - sorted_scores[0]
        confidence = "High" if gap > 10 else "Medium"

    return {
        "focus_area": selected_category,
        "recommended_retro": recommended_retro,
        "reason": reason,
        "confidence": confidence,
    }


def get_retro_health_analysis() -> dict | None:
    sprint_df = st.session_state.get("sprint_df", pd.DataFrame())
    if sprint_df.empty:
        return None

    game_df = sprint_df.copy()
    if "Defects #" not in game_df.columns:
        game_df["Defects #"] = 0
    game_df = game_df.rename(
        columns={
            "Sprint": "Sprint Name",
            "Committed": "Committed SP",
            "Completed": "Completed SP",
        }
    )

    mood_sheet_error = ""
    try:
        mood_by_sprint = get_mood_score_by_sprint_name()
    except Exception as error:
        mood_by_sprint = {}
        mood_sheet_error = str(error)

    retro_df, missing_fields = prepare_retro_health_dataframe(game_df)
    if retro_df is None:
        return {
            "error": "Input data is missing required fields: " + ", ".join(missing_fields),
            "mood_sheet_error": mood_sheet_error,
            "mood_by_sprint": mood_by_sprint,
        }

    retro_df = retro_df.tail(6).copy()
    fallback_people_score = normalize_people_score(get_default_team_morale())

    raw_people_mood = retro_df["Sprint Name"].map(mood_by_sprint)
    carried_people_mood = raw_people_mood.ffill()
    missing_after_carry = carried_people_mood.isna()

    retro_df["People Mood (1-5)"] = carried_people_mood.fillna(float(get_default_team_morale()))
    retro_df["People Mood Source"] = "Direct mood match"
    retro_df.loc[raw_people_mood.isna(), "People Mood Source"] = "Previous sprint mood"
    retro_df.loc[missing_after_carry, "People Mood Source"] = "Default mood fallback"

    retro_df["People Mood (1-5)"] = retro_df["People Mood (1-5)"].clip(lower=1, upper=5)
    retro_df["People Score"] = retro_df["People Mood (1-5)"].apply(
        lambda value: normalize_people_score(int(round(float(value))))
    )
    if retro_df["People Score"].isna().all():
        retro_df["People Score"] = fallback_people_score

    retro_df["Final Score"] = (
        retro_df["Delivery Score"] * RETRO_CATEGORY_WEIGHTS["Delivery"]
        + retro_df["Quality Score"] * RETRO_CATEGORY_WEIGHTS["Quality"]
        + retro_df["Inventory Score"] * RETRO_CATEGORY_WEIGHTS["Inventory"]
        + retro_df["Productivity Score"] * RETRO_CATEGORY_WEIGHTS["Productivity"]
        + retro_df["People Score"] * RETRO_CATEGORY_WEIGHTS["People"]
    )

    category_scores = {
        "Delivery": float(retro_df["Delivery Score"].mean()),
        "Quality": float(retro_df["Quality Score"].mean()),
        "Inventory": float(retro_df["Inventory Score"].mean()),
        "Productivity": float(retro_df["Productivity Score"].mean()),
        "People": float(retro_df["People Score"].mean()),
    }
    ai_reco = get_ai_retro_recommendation(category_scores)

    return {
        "retro_df": retro_df,
        "category_scores": category_scores,
        "ai_reco": ai_reco,
        "sprint_count": len(retro_df),
        "mood_sheet_error": mood_sheet_error,
        "mood_by_sprint": mood_by_sprint,
    }


